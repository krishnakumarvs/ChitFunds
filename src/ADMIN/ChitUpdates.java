                  jXDatePicker1.setDate(new Date(Long.parseLong(date_commence.trim())));

                String date_termination =rs.getString("reg_close_date");
                //int b=  Integer.parseInt(shared.SharedServices.convertDate(date_termination));
                 jXDatePicker2.setDate(new Date(Long.parseLong(date_termination.trim())));

                 String auction_date = rs.getString("auction_date");
                 jXDatePicker4.setDate(new Date(Long.parseLong(date_termination.trim())));
                 
                 String Chit_start_date = rs.getString("chit_start_date");
                 jXDatePicker3.setDate(new Date(Long.parseLong( Chit_start_date.trim())));
                 
                 String time = rs.getString("time");
