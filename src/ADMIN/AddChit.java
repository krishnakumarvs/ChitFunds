                    + "'" + name + "' , "
                    + "'" + className + "' , "
                    + "'" + frequency + "' , "
                    + "" + no_of_installments + " , "
                    + "" + subscription_amount + " , "
                    + "" + total_amount + " , "
                    +""   +chit_start_date+" , "
                    + "'" + dateOfCommence + 
                    "' , "
                    + "'" + date_termination + "' , "
                    + "'" + auction_date + "',  "
                     + "'" + time + "',  "
                      + "'" + payment_date + "'  "
                    + ")";
