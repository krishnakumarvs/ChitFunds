    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged

        String chitName = jComboBox2.getSelectedItem().toString();
        ResultSet rs = new Dbcon().select("select * from chittis where name='"+chitName.trim()+"' ");
        try {
            
            if (rs.next()) {
                String acutionDate = rs.getString("auction_date");
                
                if(acutionDate!=null) {
                    Date auctionDate = new Date(Long.parseLong(acutionDate.trim()));
                    jXDatePicker1.setDate(auctionDate);
                } 
                
                String auctionType = rs.getString("frequency");
                
                jTextField1.setText(auctionType);
               
            }
            
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
// TODO add your handling code here:
    }//GEN-LAST:event_jComboBox2ItemStateChanged

