    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String uname=uname_txtfild.getText();
        if(uname.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "UserName is Required");
        }
        String pwd=jPasswordField1.getText();
         if(pwd.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Password is Required");
        }
        String conf_pwd=jPasswordField2.getText();
         if(conf_pwd.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Confirm Password is Required");
        }
        
        String query="update user_details set username='"+uname+"',password='"+pwd+"',conf_password='"+conf_pwd+"', address_proof=?, age_proof=?, income_proof=? where user_id= '"+ userid +"'";
         try 
         {

                InputStream inputStream1, inputStream2, inputStream3;
                if (addressProof.trim().equals("")) 
                {
                    inputStream1 = null;
                } else 
                {
                    inputStream1 = new FileInputStream(new File(addressProof));
                }

                if (ageCertificate.trim().equals("")) 
                {
                    inputStream2 = null;
                } else 
                {
                    inputStream2 = new FileInputStream(new File(ageCertificate));
                }

                if (annualIncome.trim().equals("")) 
                {
                    inputStream3 = null;
                } 
                else 
                {
                    inputStream3 = new FileInputStream(new File(annualIncome));
                }
                PreparedStatement statement = new Dbcon().getConnection().prepareStatement(query);
               statement.setBlob(1, inputStream1);
                statement.setBlob(2, inputStream2);
                statement.setBlob(3, inputStream3);
                int executeUpdate = statement.executeUpdate();
                System.out.println(query);
                if (executeUpdate > 0) 
                {
                    JOptionPane.showMessageDialog(rootPane, "User registration completed");
                   LoginPage loginCustomer = new LoginPage();
                    loginCustomer.setVisible(true);
                    this.dispose();
                  }
                else 
                {
                    JOptionPane.showMessageDialog(rootPane, "User registration failed");
                }
         }
         catch (Exception e) {
                e.printStackTrace();
            }

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        JFileChooser fc = new JFileChooser();
        FileFilter imageFilter = new FileNameExtensionFilter(
                "Image files", ImageIO.getReaderFileSuffixes());
        fc.setFileFilter(imageFilter);
        int result = fc.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            String filePath = file.getAbsolutePath();
             jTextField1.setText(filePath);
            File source_file = file;
            Toolkit toolkit = Toolkit.getDefaultToolkit();
            addressProof = filePath;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

