    private void fetchMyAuctionDetails() {
        try {
            ResultSet rs = new Dbcon().select("select chitti_apply.*, chit.name as chit_name from chitti_apply, chittis as chit  where chitti_apply.user_id =" + UserHomepage.userId + " and chitti_apply.status=1 and chitti_apply.chitti_id = chit.id");

            while (rs.next()) {
                jComboBox1.addItem(rs.getString("chit_name"));
                chitIds.add(rs.getString("chitti_id"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

