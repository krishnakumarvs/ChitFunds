    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String current_pwd = current_pwd_textfield.getText();
         if(current_pwd.equals(""))
         {
             JOptionPane.showMessageDialog(rootPane,"Current Password Required");
         }
        String new_pwd = new_pwd_textfield.getText();
        if(new_pwd.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "New Password is Required");
        }
        String confirm_pwd = confirm_pwd_textfield.getText();
        if(confirm_pwd.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Confirm Password is Required");
        }
        Dbcon dbcon = new Dbcon();
        String query1 = "select password from  user_details where user_id = " + UserHomepage.userId;
        ResultSet rs1 = dbcon.select(query1);
        String password = "";
        try {

            if (rs1.next()) 
            {
                password = rs1.getString("password");
            } 
            else 
            {
                //System.out.println("Invalid User...");
                JOptionPane.showMessageDialog(rootPane, "Invalid query..");
            }
        } 
        catch (Exception e)
        {
            System.out.println(e);
        }
        if (current_pwd.equals(password))
        {
            if (new_pwd.equals(confirm_pwd))
            {
                String query = "update user_details set password='" + new_pwd + "', conf_password='"+new_pwd+"'";

                int rs = dbcon.insert(query);
                if (rs != 0) {
                    JOptionPane.showMessageDialog(rootPane, "Password Successfully changed..");
                  LoginPage login= new LoginPage();
                    login.setVisible(true);
                    this.dispose();
                } 
                else 
                {
                    System.out.println("failed..");
                }
            }
            else 
            {
                JOptionPane.showMessageDialog(rootPane, "Password not maching..");
            }

        }
        else 
        {
            JOptionPane.showMessageDialog(rootPane, "Password not correct..");
        }

        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

